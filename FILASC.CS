using System;

class Program
{
    static void Main()
    {
        int f = 3; 
        int c = 3; 

        int[,] twoDarr = {
            {1, 2, 3},
            {4, 5, 6},
            {7, 8, 9}
        };

        Console.WriteLine("PROGRAMA DE RECORRIDO DE FILAS");

        for (int i = 0; i < f; i++)
        {
            Console.Write($"Fila {i + 1}: ");
            for (int j = 0; j < c; j++)
            {
                Console.Write(twoDarr[i, j] + " ");
            }
            Console.WriteLine();
        }
    }
}